export async function createRefund(payload: CreateRefundInput) {
  const currentUser = await getSessionOrRedirect()
  const supabase = createClient()

  if (!payload.return_id || payload.amount <= 0) {
    return { error: "Return ID and positive amount are required", data: null }
  }

  // Verify return exists and belongs to user
  const { data: returnData, error: returnError } = await supabase
    .from("returns")
    .select("id, total")
    .eq("id", payload.return_id)
    .eq("user_id", currentUser.id)
    .single()

  if (returnError || !returnData) {
    return { error: "Return not found", data: null }
  }

  // Get existing refunds total (filter by user_id)
  const { data: existingRefunds } = await supabase
    .from("refunds")
    .select("amount")
    .eq("return_id", payload.return_id)
    .eq("user_id", currentUser.id)

  const totalRefunded = (existingRefunds ?? []).reduce((sum, r) => sum + Number(r.amount ?? 0), 0)
  const returnTotal = Number(returnData.total ?? 0)

  if (totalRefunded + payload.amount > returnTotal) {
    return {
      error: `Refund amount exceeds return total. Maximum refundable: ${returnTotal - totalRefunded}`,
      data: null,
    }
  }

  const { data: refundData, error: refundError } = await supabase
    .from("refunds")
    .insert({
      return_id: payload.return_id,
      amount: payload.amount,
      method: payload.method,
      reference: payload.reference || null,
      user_id: currentUser.id,
    })
    .select("id")
    .single()

  if (refundError || !refundData) {
    return { error: refundError?.message ?? "Unable to create refund", data: null }
  }

  revalidatePath("/returns")
  revalidatePath("/returns/refunds")
  revalidatePath("/accounts-management")
  return { error: null, data: { refundId: refundData.id } }
}

export async function getRefunds(returnId?: string, dateFrom?: string, dateTo?: string) {
  const currentUser = await getSessionOrRedirect()
  const supabase = createClient()

  let query = supabase
    .from("refunds")
    .select(
      `
      id,
      return_id,
      amount,
      method,
      reference,
      created_at,
      returns:return_id (
        id,
        return_number,
        type,
        total,
        parties:party_id (
          id,
          name
        )
      )
    `,
    )
    .eq("user_id", currentUser.id)
    .order("created_at", { ascending: false })

  if (returnId) {
    query = query.eq("return_id", returnId)
  }

  if (dateFrom) {
    const from = dateFrom.includes("T") ? dateFrom : `${dateFrom}T00:00:00.000Z`
    query = query.gte("created_at", from)
  }

  if (dateTo) {
    const to = dateTo.includes("T") ? dateTo : `${dateTo}T23:59:59.999Z`
    query = query.lte("created_at", to)
  }

  const { data, error } = await query

  if (error) {
    return []
  }

  return (data ?? []).map((row: any) => {
    const returnData = row.returns
      ? Array.isArray(row.returns)
        ? row.returns[0]
        : row.returns
      : null
    const partyData = returnData?.parties
      ? Array.isArray(returnData.parties)
        ? returnData.parties[0]
        : returnData.parties
      : null

    return {
      id: row.id,
      return_id: row.return_id,
      amount: Number(row.amount ?? 0),
      method: row.method,
      reference: row.reference,
      created_at: row.created_at,
      return: returnData
        ? {
            id: returnData.id,
            return_number: returnData.return_number,
            type: returnData.type,
            total: Number(returnData.total ?? 0),
            party: partyData
              ? {
                  id: partyData.id,
                  name: partyData.name,
                }
              : undefined,
          }
        : undefined,
    } as Refund & { return?: Return }
  })
}
