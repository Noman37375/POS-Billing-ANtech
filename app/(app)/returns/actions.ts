export async function createSaleReturn(payload: CreateSaleReturnInput) {
  const currentUser = await getSessionOrRedirect()
  const supabase = createClient()

  if (!payload.sales_invoice_id || !payload.items?.length) {
    return { error: "Sales invoice and at least one line item are required", data: null }
  }

  // Verify the sales invoice exists and belongs to user
  const { data: salesInvoice, error: invoiceError } = await supabase
    .from("sales_invoices")
    .select("id, party_id, total")
    .eq("id", payload.sales_invoice_id)
    .eq("user_id", currentUser.id)
    .single()

  if (invoiceError || !salesInvoice) {
    return { error: "Sales invoice not found", data: null }
  }

  const taxRate = payload.taxRate ?? 18
  const subtotal = payload.items.reduce((sum, item) => sum + item.quantity * item.unitPrice, 0)
  const tax = subtotal * (taxRate / 100)
  const total = subtotal + tax

  // Verify party belongs to user
  const { data: party } = await supabase
    .from("parties")
    .select("id")
    .eq("id", payload.party_id)
    .eq("user_id", currentUser.id)
    .single()

  if (!party) {
    return { error: "Party not found", data: null }
  }

  // Verify all items belong to user
  const itemIds = payload.items.map((item) => item.itemId)
  const { data: items } = await supabase
    .from("inventory_items")
    .select("id")
    .in("id", itemIds)
    .eq("user_id", currentUser.id)

  if (!items || items.length !== itemIds.length) {
    return { error: "One or more items not found", data: null }
  }

  // Create return (return_number will be auto-generated by trigger)
  const { data: returnData, error: returnError } = await supabase
    .from("returns")
    .insert({
      type: "sale",
      sales_invoice_id: payload.sales_invoice_id,
      purchase_invoice_id: null,
      party_id: payload.party_id,
      subtotal,
      tax,
      total,
      status: "Completed", // Auto-complete returns
      user_id: currentUser.id,
    })
    .select("id, return_number")
    .single()

  if (returnError || !returnData) {
    return { error: returnError?.message ?? "Unable to create return", data: null }
  }

  // Create return lines
  const lineItems = payload.items.map((item) => ({
    return_id: returnData.id,
    item_id: item.itemId,
    quantity: item.quantity,
    unit_price: item.unitPrice,
    line_total: item.quantity * item.unitPrice,
    sales_invoice_line_id: item.salesInvoiceLineId || null,
    purchase_invoice_line_id: null,
  }))

  const { error: lineError } = await supabase.from("return_lines").insert(lineItems)
  if (lineError) {
    return { error: lineError.message, data: null }
  }

  // Reverse stock (add stock back) - Sale return means stock comes back IN
  try {
    await Promise.all(
      payload.items.map(async (item) => {
        const { error: incrementError } = await supabase.rpc("increment_inventory_stock", { item_id: item.itemId, quantity: item.quantity })
        if (incrementError) {
          throw new Error(`Failed to restore stock for item ${item.itemId}: ${incrementError.message}`)
        }

        await recordStockMovement({
          itemId: item.itemId,
          movementType: "IN",
          quantity: item.quantity,
          referenceType: "SaleReturn",
          referenceId: returnData.id,
          notes: `Sale return ${returnData.return_number}`,
          userId: currentUser.id,
        })
      }),
    )
  } catch (error) {
    return { error: error instanceof Error ? error.message : "Failed to process inventory", data: null }
  }

  // Process refunds if provided
  if (payload.refunds && payload.refunds.length > 0) {
    const refundRows = payload.refunds
      .filter((r) => r.amount > 0)
      .map((r) => ({
        return_id: returnData.id,
        amount: r.amount,
        method: r.method,
        reference: r.reference || null,
        user_id: currentUser.id, // Add user_id for multi-tenant isolation
      }))

    if (refundRows.length > 0) {
      const { error: refundError } = await supabase.from("refunds").insert(refundRows)
      if (refundError) {
        // Don't fail the return if refund fails, but log it
        console.error("Error creating refunds:", refundError)
      }
    }
  }

  revalidatePath("/returns")
  revalidatePath("/returns/sales")
  revalidatePath("/returns/reports")
  revalidatePath("/accounts-management")
  revalidatePath("/dashboard")
  return { error: null, data: { returnId: returnData.id, returnNumber: returnData.return_number } }
}

