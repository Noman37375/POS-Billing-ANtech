  const supabase = createClient()

  const { data: returnData, error: returnError } = await supabase
    .from("returns")
    .select(
      `
      id,
      return_number,
      type,
      sales_invoice_id,
      purchase_invoice_id,
      party_id,
      subtotal,
      tax,
      total,
      status,
      created_at,
      updated_at,
      parties:party_id (
        id,
        name,
        phone
      )
    `,
    )
    .eq("id", returnId)
    .eq("user_id", currentUser.id)
    .single()

  if (returnError || !returnData) {
    return null
  }

  // Get return lines
  const { data: linesData } = await supabase
    .from("return_lines")
    .select(
      `
      id,
      return_id,
      item_id,
      quantity,
      unit_price,
      line_total,
      sales_invoice_line_id,
      purchase_invoice_line_id,
      created_at,
      inventory_items:item_id (
        id,
        name
      )
    `,
    )
    .eq("return_id", returnId)

  // Get refunds (filter by user_id)
  const { data: refundsData } = await supabase
    .from("refunds")
    .select("*")
    .eq("return_id", returnId)
    .eq("user_id", currentUser.id)
